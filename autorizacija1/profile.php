      <h2>Įkūrimas</h2>
      <p>Po Lietuvos nepriklausomybės paskelbimo iškilo poreikis suformuoti ginkluotąsias pajėgas. Vienas iš pirmųjų tokių pajėgų buvo Aukščiausiosios Tarybos Apsaugos skyrius. Jo paskirtis buvo apsaugoti Lietuvos Seimą. Bėgant metams Lietuvos kariuomenė buvo pertvarkyta daugelį kartų. Po Kauno Vytauto Didžiojo motorizuoto pėstininkų bataliono pertvarkymo 1995 m., buvo sukurtas atskiras Vytauto Didžiojo jėgerių batalionas, kuris vėliau tapo vienu iš pagrindinių SOP dalinių.

      Neoficialus SOP formavimas prasidėjo 1995 m., po dvejų metų jos buvo pilnai suformuotos. Po rugsėjo 11 d. išpuolių pagrindinė dalinio paskirtis buvo užkirsti kelią teroristų išpuoliams, sunaikinti teroristus ir išgelbėti įkaitus. Vytauto Didžiojo jėgerių bataliono kariai vadinami „Jėgeriais“. Ypatingosios paskirties tarnybos kariai vadinami žaliukais.

      Po 2007 m. pertvarkymo, buvo sukurta nauja Kovinių narų tarnyba, kurios užduotys buvo povandeninės ir vandens paviršiaus operacijos.

      Tarp slapčiausių SOP dalinių yra eskadronai, vadinami Aitvaras. Pirmą kartą šių eskadronų buvimas viešai pripažintas 2000 m., kai lietuvių jūreiviai buvo pagrobti Pusiaujo Gvinėjoje. Aitvaras vykdo itin slaptas užduotis.</p>
