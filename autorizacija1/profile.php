      <p>sOP rengiasi vykdyti aukštos rizikos ir aukštos pridėtinės vertės operacijas, kuriomis siekiama strateginių ir operacinių rezultatų politinėje, karinėje, ekonominėje ir informacinėje plotmėje. Atsižvelgiant į šiuolaikinio karo patirtį, ypatingas dėmesys skiriamas gebėjimams veikti nekonvencinio karo metu. SOP gali būti pasitelktos Lietuvos teritorijoje teroristinių atakų atvejais, kai teisėtvarkos institucijos neturi reikiamų pajėgumų ar jų neužtenka reaguoti į teroristines atakas.



