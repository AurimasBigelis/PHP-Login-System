          <h1>Lietuvos kariuomenės Specialiųjų operacijų pajėgos</h1>
      <p>  Lietuvos specialiųjų operacijų pajėgos (SOP) – aukštos parengties ir itin gerai ginkluotas Lietuvos kariuomenės junginys, vykdantis įvairias operacijas ir kovines užduotis. Jį sudaro kruopščiai atrinkti, motyvuoti ir specialiai apmokyti profesionalai. Lietuvos SOP veikė de facto nuo 1997 m., o de jure įkurtas 2002 m. balandžio 3 d. Iki 2008 m. balandžio 3 d. vadinosi „Specialiųjų operacijų junginys“. Vienas pagrindinių SOP tikslų yra antiteroristinės misijos už Lietuvos ribų. SOP vykdo ir kitokias misijas: tiesioginio veiksmo, specialioji žvalgyba, įkaitų gelbėjimas ir VIP apsauga. SOP gali veikti ir Lietuvos teritorijoje, kai teisėsaugos institucijos neturi galimybių reaguoti į teroristų išpuolius.</p>
